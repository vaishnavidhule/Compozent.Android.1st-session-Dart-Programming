### **7. Working with Sets**
Write a Dart program to:  
1. Create a set of unique integers: `{1, 2, 3, 4}`.  
2. Add a new number `5` to the set.  
3. Remove the number `2` from the set.  
4. Print the final set.

**Expected Output:**
```
Final Set: {1, 3, 4, 5}
```

