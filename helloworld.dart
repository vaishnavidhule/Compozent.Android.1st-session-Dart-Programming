### **2. Simple Arithmetic Operations**
Write a Dart program to perform and print the following operations on two numbers `a` and `b`:  
- Sum  
- Difference  
- Product  
- Quotient  

Assign `a = 10` and `b = 5`.

**Expected Output:**
```
Sum: 15  
Difference: 5  
Product: 50  
Quotient: 2.0
```

